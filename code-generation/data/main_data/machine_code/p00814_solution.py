def life_line(N, C, S):
    points = 0
    for i in range(N):
        for j in range(i+1):
            if S[i][j] == C:
                if i == N-1 or (S[i+1][j] != 0 and S[i+1][j+1] != 0):
                    points += S[i][j]
                    S[i][j] = 0
    return points

